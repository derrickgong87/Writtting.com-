import React, { useState } from 'react';
import { X, Mail, Lock, ArrowRight, User, ChevronLeft, ShieldCheck, Globe } from 'lucide-react';
import { TermsOfServiceText } from './LegalDocs';

interface AuthModalProps {
  isOpen: boolean;
  onClose: () => void;
  onSuccess: () => void;
}

// High-fidelity Google Icon
const GoogleIcon = () => (
  <svg width="20" height="20" viewBox="0 0 24 24" fill="none" xmlns="http://www.w3.org/2000/svg">
    <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" fill="#4285F4"/>
    <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853"/>
    <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l3.66-2.84z" fill="#FBBC05"/>
    <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335"/>
  </svg>
);

export const AuthModal: React.FC<AuthModalProps> = ({ isOpen, onClose, onSuccess }) => {
  const [isLogin, setIsLogin] = useState(true);
  const [showTerms, setShowTerms] = useState(false);
  const [showGoogleSim, setShowGoogleSim] = useState(false);
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [isLoading, setIsLoading] = useState(false);
  
  const [suggestions, setSuggestions] = useState<string[]>([]);
  const commonDomains = ['gmail.com', 'yahoo.com', 'outlook.com', 'hotmail.com', 'icloud.com', 'edu.com'];

  const handleEmailChange = (e: React.ChangeEvent<HTMLInputElement>) => {
      const val = e.target.value;
      setEmail(val);
      if (val.includes('@')) {
          const [prefix, suffix] = val.split('@');
          if (suffix !== undefined) {
             const filtered = commonDomains.filter(d => d.startsWith(suffix));
             setSuggestions(filtered.map(d => `${prefix}@${d}`));
          } else {
             setSuggestions([]); 
          }
      } else {
          setSuggestions([]);
      }
  };

  const handleSubmit = (e: React.FormEvent) => {
    e.preventDefault();
    setIsLoading(true);
    setTimeout(() => {
        setIsLoading(false);
        onSuccess();
    }, 1000);
  };

  const handleGoogleAuth = () => {
    setShowGoogleSim(true);
  };

  const completeGoogleAuth = () => {
    setIsLoading(true);
    setTimeout(() => {
        setIsLoading(false);
        setShowGoogleSim(false);
        onSuccess();
    }, 1200);
  };

  if (!isOpen) return null;

  // --- Simulated Google Login Screen ---
  if (showGoogleSim) {
      return (
        <div className="fixed inset-0 z-[100] flex items-center justify-center p-4">
            <div className="absolute inset-0 bg-slate-900/60 backdrop-blur-sm transition-opacity" onClick={() => setShowGoogleSim(false)}></div>
            <div className="relative bg-white rounded-3xl shadow-2xl w-full max-w-md overflow-hidden animate-in fade-in zoom-in-95 duration-200">
                <div className="p-8 text-center border-b border-slate-100">
                    <div className="flex justify-center mb-6">
                        <GoogleIcon />
                    </div>
                    <h2 className="text-xl font-bold text-slate-900 mb-1">Sign in with Google</h2>
                    <p className="text-sm text-slate-500">to continue to Humanizer.help</p>
                </div>
                
                <div className="p-8 space-y-4">
                    <button 
                        onClick={completeGoogleAuth}
                        className="w-full flex items-center gap-4 p-4 hover:bg-slate-50 border border-slate-200 rounded-2xl transition-all group"
                    >
                        <div className="w-10 h-10 rounded-full bg-slate-100 flex items-center justify-center text-slate-600 font-bold overflow-hidden border border-slate-200">
                            <img src="https://api.dicebear.com/7.x/notionists/svg?seed=Felix" alt="User" />
                        </div>
                        <div className="text-left">
                            <div className="text-sm font-bold text-slate-900">Felix Humanizer</div>
                            <div className="text-xs text-slate-500">felix.humanizer@gmail.com</div>
                        </div>
                    </button>

                    <button className="w-full flex items-center gap-4 p-4 hover:bg-slate-50 border border-slate-200 rounded-2xl transition-all group">
                        <div className="w-10 h-10 rounded-full bg-slate-50 flex items-center justify-center text-slate-400">
                            <User size={20} />
                        </div>
                        <div className="text-left">
                            <div className="text-sm font-bold text-slate-700">Use another account</div>
                        </div>
                    </button>
                </div>

                <div className="px-8 pb-8 text-[11px] text-slate-400 leading-relaxed">
                    To continue, Google will share your name, email address, language preference, and profile picture with Humanizer.help. Before using this app, you can review its <span className="text-brand-600">Privacy Policy</span> and <span className="text-brand-600">Terms of Service</span>.
                </div>
            </div>
        </div>
      );
  }

  if (showTerms) {
      return (
        <div className="fixed inset-0 z-[100] flex items-center justify-center p-4">
          <div className="absolute inset-0 bg-slate-900/60 backdrop-blur-sm transition-opacity" onClick={onClose}></div>
          <div className="relative bg-white rounded-3xl shadow-2xl w-full max-w-lg overflow-hidden flex flex-col max-h-[90vh]">
            <div className="h-20 bg-slate-900 p-6 flex items-center gap-4 shrink-0">
                <button onClick={() => setShowTerms(false)} className="text-white/70 hover:text-white transition-colors">
                    <ChevronLeft size={24} />
                </button>
                <h2 className="text-xl font-bold text-white">Terms of Service</h2>
            </div>
            <div className="p-8 overflow-y-auto"><TermsOfServiceText /></div>
          </div>
        </div>
      );
  }

  return (
    <div className="fixed inset-0 z-[100] flex items-center justify-center p-4">
      <div className="absolute inset-0 bg-slate-900/70 backdrop-blur-md transition-opacity" onClick={onClose}></div>

      <div className="relative bg-white rounded-[2.5rem] shadow-2xl w-full max-w-md overflow-hidden animate-in fade-in zoom-in-95 duration-300">
        
        <div className="h-32 bg-slate-900 p-8 flex justify-between items-start relative overflow-hidden">
          <div className="absolute top-0 right-0 w-64 h-64 bg-brand-500/20 rounded-full blur-3xl -mr-20 -mt-20"></div>
          <div className="relative z-10">
             <h2 className="text-2xl font-black text-white leading-tight">
               {isLogin ? 'Welcome Back' : 'Create Account'}
             </h2>
             <p className="text-slate-400 text-xs mt-1 font-medium tracking-wide uppercase">
               {isLogin ? 'Sign in to continue' : 'Join 100k+ users today'}
             </p>
          </div>
          <button 
            onClick={onClose}
            className="text-white/50 hover:text-white transition-colors bg-white/10 hover:bg-white/20 rounded-full p-2 relative z-10"
          >
            <X size={20} />
          </button>
        </div>
        
        <div className="p-8 pt-10">
          <div className="space-y-6">
             {/* Google Auth Button */}
             <button 
                onClick={handleGoogleAuth}
                className="w-full flex items-center justify-center gap-4 px-6 py-4 bg-white border-2 border-slate-100 rounded-2xl text-slate-700 font-bold text-base hover:bg-slate-50 hover:border-slate-200 transition-all shadow-sm hover:shadow-md active:scale-[0.98] group"
             >
                <GoogleIcon />
                <span>Continue with Google</span>
             </button>

             <div className="flex items-center gap-4 py-2">
                <div className="flex-1 h-px bg-slate-100"></div>
                <span className="text-[10px] font-black text-slate-300 uppercase tracking-[0.2em]">OR LOGIN WITH EMAIL</span>
                <div className="flex-1 h-px bg-slate-100"></div>
             </div>

             <form onSubmit={handleSubmit} className="space-y-4">
                {!isLogin && (
                   <div className="space-y-1">
                     <label className="text-xs font-bold text-slate-400 uppercase tracking-wider ml-1">Full Name</label>
                     <div className="relative">
                        <User className="absolute left-4 top-1/2 -translate-y-1/2 text-slate-300" size={18} />
                        <input 
                          type="text" 
                          placeholder="John Doe"
                          className="w-full pl-12 pr-4 py-3.5 bg-slate-50 border-2 border-transparent rounded-2xl focus:bg-white focus:border-brand-500/20 focus:ring-4 focus:ring-brand-500/5 transition-all text-sm font-medium outline-none"
                        />
                     </div>
                   </div>
                )}

                <div className="space-y-1">
                  <label className="text-xs font-bold text-slate-400 uppercase tracking-wider ml-1">Email Address</label>
                  <div className="relative">
                     <Mail className="absolute left-4 top-1/2 -translate-y-1/2 text-slate-300" size={18} />
                     <input 
                       type="email" 
                       value={email}
                       onChange={handleEmailChange}
                       placeholder="you@example.com"
                       className="w-full pl-12 pr-4 py-3.5 bg-slate-50 border-2 border-transparent rounded-2xl focus:bg-white focus:border-brand-500/20 focus:ring-4 focus:ring-brand-500/5 transition-all text-sm font-medium outline-none"
                       required
                       autoComplete="off"
                     />
                  </div>
                </div>

                <div className="space-y-1">
                  <div className="flex justify-between items-end mb-1">
                    <label className="text-xs font-bold text-slate-400 uppercase tracking-wider ml-1">Password</label>
                    {isLogin && (
                        <button type="button" className="text-[10px] font-bold text-brand-600 hover:text-brand-700 uppercase tracking-wide">Forgot Password?</button>
                    )}
                  </div>
                  <div className="relative">
                     <Lock className="absolute left-4 top-1/2 -translate-y-1/2 text-slate-300" size={18} />
                     <input 
                       type="password" 
                       value={password}
                       onChange={(e) => setPassword(e.target.value)}
                       placeholder="••••••••"
                       className="w-full pl-12 pr-4 py-3.5 bg-slate-50 border-2 border-transparent rounded-2xl focus:bg-white focus:border-brand-500/20 focus:ring-4 focus:ring-brand-500/5 transition-all text-sm font-medium outline-none"
                       required
                     />
                  </div>
                </div>

                <button 
                  type="submit"
                  disabled={isLoading}
                  className="w-full bg-brand-600 hover:bg-brand-700 text-white font-bold py-4 rounded-2xl shadow-xl shadow-brand-600/20 transition-all flex items-center justify-center gap-2 mt-4 disabled:opacity-50 group"
                >
                  {isLoading ? (
                    <div className="w-5 h-5 border-2 border-white/30 border-t-white rounded-full animate-spin"></div>
                  ) : (
                    <>
                        {isLogin ? 'Sign In' : 'Create Account'}
                        <ArrowRight size={18} className="group-hover:translate-x-1 transition-transform" />
                    </>
                  )}
                </button>
             </form>

             <div className="flex justify-center items-center gap-2 pt-4">
                 <span className="text-sm text-slate-400 font-medium">
                   {isLogin ? "Don't have an account?" : "Already have an account?"}
                 </span>
                 <button 
                   onClick={() => setIsLogin(!isLogin)}
                   className="text-sm font-bold text-brand-600 hover:text-brand-700 transition-colors"
                 >
                   {isLogin ? 'Sign up' : 'Log in'}
                 </button>
             </div>
          </div>
          
          <div className="mt-8 pt-8 border-t border-slate-100 flex items-center justify-center gap-2 text-[10px] text-slate-400 font-bold uppercase tracking-[0.1em]">
            <ShieldCheck size={14} className="text-emerald-500" /> Secure SSL Encryption
          </div>
        </div>
      </div>
    </div>
  );
};